import Foundation
import UIKit

extension UIFont {
    
    static func metropolisMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Metropolis-Medium", size: size)
    }
    
    static func metropolisBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Metropolis-Bold", size: size)
    }
    
    static func metropolisRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "Metropolis-Regular", size: size)
    }
    
    
}
